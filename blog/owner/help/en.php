    <p>Post ::<strong><span id="Write">Write</span></strong></p>
    <blockquote>
        <p>You can write  your blog entry with a WYSIWYG (What You See Is What You Get) editor. </p>
        <p>To give the location  of the entry, you can enter location “tags”. <br />
            Here are some  tips on entering the location tags: </p>
        <p>1. In the space  provided, input the highest level of geographic domain--usually the country  (Example: France).  As you type, you will see the list of recommended keywords below--you can  choose one from the list or continue typing. <br />
            <br />
            2. Press the Enter key, and another space will pop up next, so you can further  specify the location (Example: France  &gt; Paris).<br />
            <br />
            3. You can continue this process to narrow down the location. (Example: France &gt; Paris  &gt; Eiffel Tower). To go one step back, you can  press the Backspace key. <br />
            <br />
            4. If you believe you have successfully designated the location, you can press  the Tab key at any level to exit. </p>
    </blockquote>
